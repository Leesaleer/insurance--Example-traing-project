						<h2>4. Long-term care riders</h2> Some life products include this option, which allows you to use the benefits of your policy to pay for long-term care in exchange for a reduced life benefit.  

						<h2>5. Spouse or child term riders</h2>  Life policies with this feature allow you to purchase term life insurance for your spouse or dependent child, up to age 26.  This option can be a more affordable way to purchase coverage if you can't afford separate policies.
